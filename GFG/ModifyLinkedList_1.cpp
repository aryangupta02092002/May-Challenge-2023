class Solution{
    public:
    struct Node* modifyTheList(struct Node *head)
    {
        //add code here.
        vector<int> v;
        Node* curr = head;
        while(curr != NULL){
            v.push_back(curr->data);
            curr = curr->next;
        }
        int n = v.size();
        int i = 0;
        int j = n-1;
        curr = head;
        while(i < j){
            curr->data = v[j] - v[i];
            i++;
            j--;
            curr = curr->next;
        }
        while(curr != NULL){
            curr->data = v[j];
            curr = curr->next;
            j--;
        }
        return head;
    }
};

